    // Method to validate the user in the database
    private boolean validateUser(String username, String email) {
        boolean isValid = false;

        // Implement your database connection and query here
        String query = "EXEC CheckUsernameAndEmailID ?, ?";

        try (Connection connection = DBConnection.getConnection(); // Your DB connection method
             PreparedStatement preparedStatement = connection.prepareStatement(query)) {

            preparedStatement.setString(1, username);
            preparedStatement.setString(2, email);

            ResultSet resultSet = preparedStatement.executeQuery();

            if (resultSet.next()) {
                int userCount = resultSet.getInt("UserCount");
                isValid = (userCount > 0); // Valid if userCount is greater than 0
            }

        } catch (SQLException ex) {
            ex.printStackTrace(); // Handle exceptions appropriately
        }

        return isValid;
    }

    
    // Method to open the password editing page
    private void openPasswordEditingPage(String username) {
        // Code to open the password editing form
        new PasswordEditingForm(username).setVisible(true); // Open PasswordEditingForm with the username
        this.dispose();
    }


