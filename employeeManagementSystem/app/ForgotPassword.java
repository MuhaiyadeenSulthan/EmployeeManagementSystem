    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        // TODO add your handling code here:
        String username = txt_uname.getText().trim();
        String email = txt_email.getText().trim();
        
        if (username.isEmpty() || email.isEmpty()) {
            lbl_message.setText("Please fill in both fields.");
            return;
        }

        // Call method to validate username and email
        if (validateUser(username, email)) {
            // If valid, navigate to the password editing page
            openPasswordEditingPage(username);
        } else {
            // If invalid, show error message
            lbl_message.setText("Invalid username or email.");
        }
    }//GEN-LAST:event_btn_submitActionPerformed

