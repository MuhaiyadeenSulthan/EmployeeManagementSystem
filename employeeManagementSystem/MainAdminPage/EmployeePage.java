private void loadLeaveData() {
    // Assuming you have a table to display leave data
    // Example: JTable leaveTable
    String username = userName;
    String query = "SELECT Sno, Leave_Type, From_Date, To_Date, Description, Applied_Date, Status FROM Leave_Tbl WHERE Username = ? ORDER BY Applied_Date DESC";

    try (Connection conn = DBConnection.getConnection()) {
        if (conn == null) {
            JOptionPane.showMessageDialog(this, "Failed to connect to the database.", "Connection Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Prepare SQL query with EmployeeEmail as the filter
        try (PreparedStatement pst = conn.prepareStatement(query)) {
            pst.setString(1, username);  // Assuming this.employeeEmail contains the logged-in employee's email

            try (ResultSet rs = pst.executeQuery()) {
                // Clear previous data from the table (if any)
                DefaultTableModel model = (DefaultTableModel) tbl_leave.getModel();
                model.setRowCount(0);  // Clears the table

                // Iterate over the result set and populate the table
                while (rs.next()) {
                    int sno = rs.getInt("Sno"); // Get Sno from the result set
                    String leaveType = rs.getString("Leave_Type");
                    Date fromDate = rs.getDate("From_Date");
                    Date toDate = rs.getDate("To_Date");
                    String description = rs.getString("Description");
                    Date appliedDate = rs.getDate("Applied_Date");
                    String status = rs.getString("Status");

                    // Add the row to the table
                    model.addRow(new Object[]{
                        sno,                       // Add the Sno in the first column
                        leaveType,
                        fromDate.toString(),       // Convert Date to String if needed
                        toDate.toString(),
                        description,
                        appliedDate.toString(),
                        status
                    });
                }
            }
        }
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
    }
}




  
