    private void fetchAndDisplaySalaryDetails(String employeeName) {
        // Define the SQL query to fetch salary details
        String sql = "SELECT SalaryID, EmployeeName, BasicSalary, MonthlyAllowance, Deduction, NetPay, PayDate FROM Salary_Tbl WHERE EmployeeName = ?";

        // Create a table model for displaying data
        DefaultTableModel tableModel = (DefaultTableModel) tbl_salary.getModel(); // Get the existing model of the table
        tableModel.setRowCount(0); // Clear the existing rows in the table before adding new ones

        try (Connection conn = DBConnection.getConnection(); PreparedStatement stmt = conn.prepareStatement(sql)) {

            stmt.setString(1, employeeName); // Set the employee name parameter
            try (ResultSet rs = stmt.executeQuery()) {
                // Populate the table with data from the result set
                while (rs.next()) {
                    Object[] row = {
                        rs.getInt("SalaryID"),
                        rs.getString("EmployeeName"),
                        rs.getDouble("BasicSalary"),
                        rs.getDouble("MonthlyAllowance"),
                        rs.getDouble("Deduction"),
                        rs.getDouble("NetPay"),
                        rs.getDate("PayDate")
                    };
                    tableModel.addRow(row);
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching salary details.", "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
